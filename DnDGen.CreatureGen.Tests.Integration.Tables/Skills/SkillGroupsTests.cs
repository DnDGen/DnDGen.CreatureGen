        private void NoDuplicationOfSkillsBetweenTypeAndCreature(string creature, string[] skills)
        {
            var data = creatureDataSelector.SelectOneFrom(Config.Name, TableNameConstants.Collection.CreatureData, creature);
            var creatureTypes = data.Types.Except([creature]);

            Assert.That(table.Keys, Contains.Item(creature)
                .And.SupersetOf(creatureTypes));

            foreach (var creatureType in creatureTypes)
            {
                var creatureSkills = table[creature];
                var creatureTypeSkills = table[creatureType];

                Assert.That(creatureSkills.Intersect(creatureTypeSkills), Is.Empty, creatureType);
            }
        }

        [TestCaseSource(typeof(CreatureTestData), nameof(CreatureTestData.Types))]
        [TestCaseSource(typeof(CreatureTestData), nameof(CreatureTestData.Subtypes))]
        public void CreatureTypeSkillsContainSkillsCommonToAllCreatures(string creatureType)
        {
            var creatures = creatureDataSelector.SelectAllFrom(Config.Name, TableNameConstants.Collection.CreatureData)
                .Where(kvp => kvp.Value.Single().Types.Contains(creatureType))
                .Select(kvp => kvp.Key);

            if (!creatures.Any())
            {
                Assert.That(creatureType, Is.EqualTo(CreatureConstants.Types.Subtypes.Augmented));
                Assert.Pass($"{CreatureConstants.Types.Subtypes.Augmented} only applies to templates");
            }

            Assert.That(table.Keys, Contains.Item(creatureType)
                .And.SupersetOf(creatures));

            var creatureSkillGroups = table.Where(kvp => creatures.Contains(kvp.Key)).Select(kvp => kvp.Value);
            var commonSkills = creatureSkillGroups.First();

            foreach (var skillGroup in creatureSkillGroups)
            {
                commonSkills = commonSkills.Intersect(skillGroup);
            }

            Assert.That(commonSkills, Is.SubsetOf(table[creatureType]));
        }
